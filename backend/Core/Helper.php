<?php

namespace App\Core;

use App\Helper\Database;

/**
 * Helper class chứa các utility functions dùng chung
 * Tất cả methods đều là static để dễ sử dụng
 */
class Helper
{
    /**
     * Lấy PDO connection từ Database singleton
     */
    public static function PDO(): \PDO
    {
        return Database::getInstance();
    }

    /**
     * Tạo base URL
     */
    public static function baseUrl(string $url = ''): string
    {
        return ($_ENV['BASE_URL'] ?? '') . $url;
    }

    /**
     * Redirect đến URL
     */
    public static function redirect(string $url): void
    {
        header('Location: ' . $url);
        exit();
    }

    /**
     * Hiển thị 403 error
     */
    public static function show403(?string $message = null): void
    {
        if ($message) {
            Session::setFlash('error_message', $message);
        }
        http_response_code(403);
    }

    /**
     * Lấy config value
     */
    public static function config(string $key, $default = null)
    {
        static $configs = null;
        if ($configs === null) {
            $configPath = __DIR__ . '/../utils/configs.php';
            $configs = file_exists($configPath) ? require $configPath : [];
        }
        return $configs[$key] ?? $default;
    }

    /**
     * Generate URL-friendly slug from text
     * @param string $text Input text
     * @param bool $handleVietnamese Whether to remove Vietnamese accents
     * @return string Slug
     */
    public static function generateSlug(string $text, bool $handleVietnamese = true): string
    {
        $slug = strtolower($text);

        if ($handleVietnamese) {
            $slug = self::removeAccents($slug);
        }

        // Replace spaces and special chars with hyphens
        $slug = preg_replace('/[^a-z0-9]+/', '-', $slug);

        // Remove leading/trailing hyphens
        $slug = trim($slug, '-');

        return $slug;
    }

